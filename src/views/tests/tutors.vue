<template>
	<div class="container">
		<helper-spinner v-if="isLoading"/>
		<div v-else>
			<div v-if="isTutor">
				<div v-if="started">
					<test :questions="tests" :level="tutor['levels'][tutor.courses[0]] + 1" :course="tutor.course" />
				</div>
				<div class="jumbotron" v-else>
					<h2 class="text-center">Level {{ tutor['levels'][tutor.courses[0]] + 1 }} for Tutors</h2>
					<p class="lead">This is a simple {{ tutor.courses[0] }} test designed to figure out how comfortable you are with {{ tutor.courses[0] }} before pairing you with students.</p>
					<hr class="my-4">
					<p>Make sure you have at least an hour to spare before starting the test.</p>
					<p>You must pass at least 70% of the test before you can become a tutor.</p>
					<button class="shadow-none" :class="failed ? 'opacity-25' : 'accent-button'" @click="startTest" :disabled="failed">Start Test</button>
					<p class="text-danger" v-if="failed">
						You took this test less than 2 hours ago and failed to meet the 70% pass mark. You can retake the test by {{ getRetakeTime }}.
					</p>
				</div>
			</div>
			<helper-message v-else message="This account is not recognized as a tutor's account"
				optional="Visit your profile settings to upgrade" />
		</div>
	</div>
</template>

<script>
	import Test from '@/components/tests/tutors/Test'
	import HelperSpinner from '@/components/helpers/Spinner'
	import HelperMessage from '@/components/helpers/Message'
	import { firestore } from '@/config/firebase'
	import { mapGetters } from 'vuex'

	export default {
		name: 'TestsTutors',
		data: () => ({
			isLoading: false,
			started: false,
			failed: false,
			questions: [],
			tests: []
		}),
		methods: {
			checkIfFailed(){
				let courseUpgrade = this.tutor.upgrade[this.tutor.course]
				let upgrade = courseUpgrade[this.tutor.level + 1]
				if(upgrade && upgrade['passed'] === false){
					let twoHoursToMs = 7200000
					let failedTime = new Date(upgrade['takenAt']['seconds'] * 1000)
					this.failed = (new Date() - failedTime) < twoHoursToMs
				}
			},
			async getAllQuestions(){
				let docs = await firestore.collection('questions')
					.where('subject','==', this.tutor.courses[0])
					.where('level','==', this.tutor['levels'][this.tutor.courses[0]] + 1)
					.orderBy('createdAt','desc')
					.limit(100)
					.get()
				docs.forEach(doc => this.questions.push({ ...doc.data(), '.key': doc.id }))
			},
			getRandomQuestions(){
				// TODO: Adjust test length to number required for testing
				let testLength = 5
				this.tests = []
				for(let i = 0; i < testLength; i++){
					if(this.questions.length === this.tests.length){
						new window.Toast({ icon: 'warning', title: 'No more questions' })
						break
					}
					let random = Math.floor(Math.random() * this.questions.length)
					let question = this.questions[random]
					while(this.tests.find(test => test['.key'] === question['.key'])){
						random = Math.floor(Math.random() * this.questions.length)
						question = this.questions[random]
					}
					this.tests.push(question)
				}
			},
			async startTest(){
				let result = await  new window.SweetAlert({
					title: 'Start test',
					text: 'Are you sure you are ready to start? This test cannot be paused when started',
					icon: 'info',
					showCancelButton: true,
					confirmButtonColor: '#3085d6',
					cancelButtonColor: '#d33',
					confirmButtonText: 'Submit'
				})
				if(result.value){
					this.isLoading = true
					await this.getAllQuestions()
					this.getRandomQuestions()
					this.isLoading = false
					this.started = true
				}
			}
		},
		computed: {
			...mapGetters(['getId','getUser']),
			tutor(){ return this.getUser.tutor || {} },
			isTutor(){ return this.getUser.roles && this.getUser.roles.isTutor },
			getRetakeTime(){
				let d = this.tutor.upgrade[this.tutor.course][this.tutor.level + 1]['takenAt']
				d = new Date(d.seconds * 1000)
				d.setHours(d.getHours() + 2)
				return d.toTimeString().slice(0,5)
			}
		},
		components: {
			'test': Test,
			'helper-message': HelperMessage,
			'helper-spinner': HelperSpinner,
		},
		mounted(){
			if(this.isTutor){
				this.checkIfFailed()
			}
		}
	}
</script>